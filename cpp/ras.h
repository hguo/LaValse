struct Event {
  uint32_t recID;
  uint16_t msgID;
  uint64_t eventTime;

  uint8_t locationType;

  // locations in LODs
  uint32_t location[5];
  // uint32_t L0;
  // uint16_t L1, L2, L3;
  // uint8_t L4;

  // size_t msgOffset, msgLength;

  uint8_t component() const {return eventTable[msgID][1];}
  uint8_t category() const {return eventTable[msgID][2];}
  uint8_t severity() const {return eventTable[msgID][3];}
  uint16_t controlActions() const {return eventTable[msgID][4];}
  uint64_t aggregateTime(uint64_t tg) const {
    return eventTime - eventTime % tg;
  }
  uint32_t aggregateTime(uint64_t T0, uint64_t tg) const {
    return (eventTime - T0) / tg;
  }

  bool checkRange() const {
    if (msgID>=NUM_MSGID) return false;
    if (locationType>=NUM_LOCTYPE) return false;
    for (int i=0; i<5; i++)
      if (location[0]>=nlocations[0]) return false;
    return true;
  }

  void print() const {
    fprintf(stderr, "recID=%d, msgID=%d, eventTime=%lld, locationType=%d, location=[%d, %d, %d, %d, %d]\n",
        recID, msgID, eventTime, locationType, 
        location[0], location[1], location[2], location[3], location[4]);
  }
};

